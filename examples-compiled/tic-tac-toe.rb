def winner(board)
  case board
  in [["X", "X", "X", *], _, _, *]
    "X"
  in [_, ["O", "O", "O", *], _, *]
    "O"
  in [["X", _, _, *], [_, "X", _, *], [_, _, "X", *], *]
    "X"
  in [["O", _, _, *], ["O", _, _, *], ["O", _, _, *], *]
    "O"
  in _
    "draw"
  end
end
